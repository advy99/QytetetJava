    public void creaMenuOperacion(){
        for(OpcionMenu opcion: OpcionMenu.values()){
            javax.swing.JMenuItem itemOpcionMenu = new javax.swing.JMenuItem();
            
            itemOpcionMenu.setText(opcion.toString());
            itemOpcionMenu.setVisible(false);
            
            jMenuOperacion.add(itemOpcionMenu);
            
            
            itemOpcionMenu.addActionListener(new java.awt.event.ActionListener() {
              public void actionPerformed(java.awt.event.ActionEvent evt) {
                 itemOpcionMenuActionPerformed(evt);
              }
            });
        }
            
            
    }
    
    
    public void creaMenuCasilla(){
        for(int i = 0; i < 20; i++){
            javax.swing.JMenuItem itemCasillaMenu = new javax.swing.JMenuItem();
            
            itemCasillaMenu.setText(Integer.toString(i));
            itemCasillaMenu.setVisible(false);
            
            jMenuCasilla.add(itemCasillaMenu);
            
            
            itemCasillaMenu.addActionListener(new java.awt.event.ActionListener() {
              public void actionPerformed(java.awt.event.ActionEvent evt) {
                 itemCasillaMenuActionPerformed(evt);
              }
            });
        }
            
            
    }
    
    public void itemOpcionMenuActionPerformed(java.awt.event.ActionEvent evt){
        boolean encontrado = false;
        
        
        for(int i = 0; i < OpcionMenu.values().length && !encontrado; i++){
            if(OpcionMenu.values()[i].toString() == evt.getActionCommand()){
                encontrado = true;
                operacionElegida = i;
            }
        }
        
        if (controlador.necesitaElegirCasilla(operacionElegida)){
            updateJMenuCasillas(operacionElegida);
            areaMensajes.setText("\nNecesitas escoger una casilla\n");
            
        }else{
            controlador.realizarOperacion(operacionElegida, 0);
        }
        
        update();
    }
    
    public void itemCasillaMenuActionPerformed(java.awt.event.ActionEvent evt){
        int casilla;
        
        casilla = Integer.parseInt(evt.getActionCommand());
        
        
        controlador.realizarOperacion(operacionElegida, casilla);
        
        
        areaMensajes.setText("");
        
        update();

    }
    
    public void updateJMenuOperaciones(){
        ArrayList<Integer> operacionesValidas = controlador.obtenerOperacionesJuegoValidas();
        
        for(int i = 0; i < jMenuOperacion.getItemCount(); i++){   
            if(operacionesValidas.contains(i)){
                jMenuOperacion.getItem(i).setVisible(true);
            }else{
                jMenuOperacion.getItem(i).setVisible(false);
            }
            
        }
    }
    
    public void updateJMenuCasillas(int opcionMenu){
        ArrayList<Integer> operacionesValidas = controlador.obtenerCasillasValidas(opcionMenu);
        
        for(int i = 0; i < jMenuCasilla.getItemCount(); i++){   
            
            if(operacionesValidas.contains(i)){
                jMenuCasilla.getItem(i).setVisible(true);
            }else{
                jMenuCasilla.getItem(i).setVisible(false);
            }
            
        }
    }
    
    public void update(){
        areaJugadorActual.setText(modelo.getJugadorActual().toString());
        areaJugadores.setText(modelo.getJugadores().toString());
        
        if(modelo.getCartaActual() != null){
            areaCartaActual.setText(modelo.getCartaActual().toString());
        }
        
        areaTablero.setText(modelo.getTablero().toString());
        
        updateJMenuOperaciones();
        //supongo que tengo que usar operacionElegida
        updateJMenuCasillas(operacionElegida);
        
        repaint();
    }
    
    public ArrayList<String> obtenerNombreJugadores(){
        DialogoNombres dialogo = new DialogoNombres(this, true);
        
        dialogo.setVisible(true);
        
        return dialogo.getNombres();
        
    }
    
    
